    public function store(Request $request)
    {
        $values = $request->all();
        // add an animal to the db
        $addUser = new User();

        $addUser->lastname  = $values['lastname'];
        $addUser->firstname  = $values['firstname'];
        $addUser->email  = $values['email'];
        $addUser->address  = $values['address'];
        $addUser->city  = $values['city'];
        $addUser->zipCode  = $values['zipCode'];
        $addUser->password = $values['password'];
        $addUser->checkPassword = $values['checkPassword'];
    }

